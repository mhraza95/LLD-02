package DesignPattern.BuilderPattern;

public class Student {

    String name;
    String email;
    int age;
    double psp;
    String phoneNo;

    public Student(String name, String email, int age, double psp, String phoneNo) {
        this.name = name;
        this.email = email;
        this.age = age;
        this.psp = psp;
        this.phoneNo = phoneNo;
    }


}
